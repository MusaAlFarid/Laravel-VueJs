                        keySplines="0 0.5 0.5 1;0 0.5 0.5 1"
                        begin="-0.2s"
                      />
                    </rect>
                    <rect x="42.5" y="30" width="15" height="40" fill="#4b9bbe">
                      <animate
                        attributeName="y"
                        repeatCount="indefinite"
                        dur="1s"
