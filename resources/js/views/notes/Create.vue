                </template>
                Save
              </button>
            </form>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      form: {
        title: "",
        description: "",
        subject: "",
      },

      loading: false,
      subjects: [],
      theErrors: [],
    };
  },
  mounted() {
    this.getSubjects();
  },

  methods: {
    async getSubjects() {
      let response = await axios.get("/api/subjects");
      if (response.status === 200) {
        this.subjects = response.data;
      }
    },

    async store() {
      this.loading = true;
      try {
        let response = await axios.post(
          "/api/notes/create-new-note",
          this.form
        );
        if ((response.status = 200)) {
          this.form.title = "";
          this.form.description = "";
          this.form.subject = "";
          this.theErrors = [];
          this.loading = false;

          this.$toasted.show(response.data.message, {
            type: "success",
            duration: "1500",
          });
        }
      } catch (e) {
        this.loading = false;
        this.$toasted.show("something went wrong", {
          type: "error",
          duration: "1500",
        });
        this.theErrors = e.response.data.errors;
      }
    },
  },
};
</script>
